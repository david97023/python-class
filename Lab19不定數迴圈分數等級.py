g=int(input())
while g != -9999:
    if  90 <= g <= 100: 
        print('A')
    elif 80 <= g <= 89:
        print('B')
    elif 70 <= g <= 79:
        print('C')
    elif 60 <= g <= 69:
        print('D')
    else:
        print('E')
    g=int(input())


